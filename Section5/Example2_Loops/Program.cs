using Example4_ClassLibraries;

namespace Example2_Loops
{
    public class Program
    {
        public static void Main(string[] args)
        {
            ///////////////////////////////////////////////////////////////////////////////////////
            ///////////////////////////////////// WHILE LOOP //////////////////////////////////////
            ///////////////////////////////////////////////////////////////////////////////////////

            int input = -1;

            while (input < 0)
            {
                input = Question.AskForInteger("Enter a postive number");
            }

            ///////////////////////////////////////////////////////////////////////////////////////
            ////////////////////////////////////// FOR LOOP ///////////////////////////////////////
            ///////////////////////////////////////////////////////////////////////////////////////

            // The three statements inside of the "for loop" parenthesis:
            // 1.) Statement happens only once, before anything else happens.
            // 2.) The condition to keep iterating, loop terminates if condition is false.
            // 3.) Statement executed each time the end is hit.

            for (int i = 0; i < 10; i++)
            {
                System.Console.WriteLine(i);
            }

            System.Console.ReadKey();

            ///////////////////////////////////////////////////////////////////////////////////////
            ///////////////////////////////////////////////////////////////////////////////////////
        }
    }
}